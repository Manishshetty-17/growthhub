let currentQuestions = [];
let currentQuestion = 0;
let score = 0;
let selectedAnswer = "";

function startQuiz(level)
{
    currentQuestions =
    [...questions[level]].sort(() => Math.random() - 0.5);

    currentQuestion = 0;
    score = 0;
    selectedAnswer = "";

    document.querySelector(".level-selection").style.display = "none";

    document.getElementById("quiz-box").classList.remove("hidden");
    document.getElementById("result-box").classList.add("hidden");

    loadQuestion();
}

function loadQuestion()
{
    const q = currentQuestions[currentQuestion];

    document.getElementById("question-number").innerText =
    `Question ${currentQuestion + 1} of ${currentQuestions.length}`;

    document.getElementById("company-name").innerText =
    q.company || "Aptitude Arena";

    document.getElementById("question").innerText =
    q.question;

    const optionsDiv =
    document.getElementById("options");

    optionsDiv.innerHTML = "";

    selectedAnswer = "";

    q.options.forEach(option =>
    {
        const btn = document.createElement("button");

        btn.innerText = option;

        btn.classList.add("option-btn");

        btn.onclick = () =>
        {
            selectedAnswer = option;

            document
            .querySelectorAll(".option-btn")
            .forEach(button =>
            button.classList.remove("selected"));

            btn.classList.add("selected");
        };

        optionsDiv.appendChild(btn);
    });
}

document
.getElementById("next-btn")
.addEventListener("click", () =>
{
    if(selectedAnswer === "")
    {
        alert("Please select an option.");
        return;
    }

    if(
        selectedAnswer ===
        currentQuestions[currentQuestion].answer
    )
    {
        score++;
    }

    currentQuestion++;

    if(currentQuestion < currentQuestions.length)
    {
        loadQuestion();
    }
    else
    {
        showResult();
    }
});

function showResult()
{
    document
    .getElementById("quiz-box")
    .classList.add("hidden");

    document
    .getElementById("result-box")
    .classList.remove("hidden");

    const totalQuestions =
    currentQuestions.length;

    const percentage =
    Math.round((score / totalQuestions) * 100);

    document
    .getElementById("score")
    .innerText =
    `Your Score: ${score}/${totalQuestions} (${percentage}%)`;

    let performance = "";

    if(percentage >= 90)
    {
        performance =
        "🏆 Outstanding! Placement Ready.";
    }
    else if(percentage >= 75)
    {
        performance =
        "🔥 Excellent Performance!";
    }
    else if(percentage >= 60)
    {
        performance =
        "👍 Good Job. Keep Practicing.";
    }
    else if(percentage >= 40)
    {
        performance =
        "📚 Average. More Practice Needed.";
    }
    else
    {
        performance =
        "⚠️ Focus on Aptitude Basics and Try Again.";
    }

    document
    .getElementById("performance")
    .innerText =
    performance;
}

function restartQuiz()
{
    document.getElementById("result-box").classList.add("hidden");

    document.querySelector(".level-selection").style.display = "block";

    currentQuestion = 0;
    score = 0;
    selectedAnswer = "";
}